<?php

namespace Sypo\Livex\Models;

use Illuminate\Database\Eloquent\Model;

class ErrorReport extends Model
{
    /*
    |--------------------------------------------------------------------------
    | GLOBAL VARIABLES
    |--------------------------------------------------------------------------
    */

    protected $table = 'sypo_livex_error_report';

    protected $fillable = [
        'code','line','message',
    ];
	

    /*
    |--------------------------------------------------------------------------
    | FUNCTIONS
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | RELATIONS
    |--------------------------------------------------------------------------
    */

	/**
	 * Get the admin user
	 */
	public function admin()
	{
		return $this->belongsTo('Aero\Admin\Models\Admin', 'admin_id');
	}

	/**
	 * Get the order
	 */
	public function order()
	{
		return $this->belongsTo('Aero\Cart\Models\Order', 'order_id');
	}

    /*
    |--------------------------------------------------------------------------
    | SCOPES
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | ACCESSORS
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | MUTATORS
    |--------------------------------------------------------------------------
    */
}
