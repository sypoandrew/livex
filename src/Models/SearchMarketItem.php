<?php

namespace Sypo\Livex\Models;

use Illuminate\Database\Eloquent\Model;

class SearchMarketItem extends Model
{
    /*
    |--------------------------------------------------------------------------
    | GLOBAL VARIABLES
    |--------------------------------------------------------------------------
    */

    protected $table = 'sypo_livex_import_item';

    /*
    |--------------------------------------------------------------------------
    | FUNCTIONS
    |--------------------------------------------------------------------------
    */


	/**
	 * User-friendly foreign key setter for parent table
	 */
	public function setProcessIdAttribute($process_id)
	{
		return $this->sypo_livex_import_id = $process_id;
	}
    /*
    |--------------------------------------------------------------------------
    | RELATIONS
    |--------------------------------------------------------------------------
    */

	/**
	 * Get the process the item belongs to
	 */
	public function process()
	{
		return $this->belongsTo('Sypo\Livex\Models\SearchMarketProcess', 'id');
	}

    /*
    |--------------------------------------------------------------------------
    | SCOPES
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | ACCESSORS
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | MUTATORS
    |--------------------------------------------------------------------------
    */
}
